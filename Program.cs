string fruit = "grapefruit";

switch (fruit)
{
    case "grapefruit":
        Console.WriteLine($"This is a grapefruit. It tastes quite bitter.");
        break;
    case "apple":
        Console.WriteLine($"This is an apple. It is sweet and comes in many colours, like green, red or yellow.");
        break;
    case "mango":
        Console.WriteLine($"This is a mango. The inside is orange and it has a sweet, succulent flesh. The best fruit.");
        break;
}